def create_knowledge_graph_nodes(data: Dict) -> List[Dict]:
    """将数据转换为知识图谱节点格式"""
    nodes = []
    node_id = 0

    # 创建主节点
    nodes.append({
        "id": node_id,
        "label": "皮影戏",
        "type": "非遗项目",
        "properties": {
            "名称": data["基本信息"]["名称"],
            "别名": ",".join(data["基本信息"]["别名"]),
            "非遗级别": data["基本信息"]["非遗级别"],
            "入选时间": data["基本信息"]["入选时间"]
        }
    })
    main_node_id = node_id
    node_id += 1

    # 历史起源节点
    history_node = {
        "id": node_id,
        "label": "历史起源",
        "type": "历史",
        "properties": {
            "起源时间": data["历史起源"]["起源时间"],
            "起源地点": data["历史起源"]["起源地点"],
            "发源地": data["历史起源"]["发源地"]
        }
    }
    nodes.append(history_node)
    node_id += 1

    # 添加历史关系
    nodes.append({
        "id": f"r_{len([n for n in nodes if isinstance(n.get('id'), str) and n['id'].startswith('r_')])}",
        "source": main_node_id,
        "target": node_id - 1,
        "relation": "起源于",
        "type": "relationship"
    })

    # 发展历程节点
    for period in data["历史起源"]["发展历程"]:
        nodes.append({
            "id": node_id,
            "label": f"{period['朝代']}-{period['时期']}",
            "type": "历史时期",
            "properties": {
                "朝代": period["朝代"],
                "时期": period["时期"],
                "描述": period["描述"]
            }
        })
        node_id += 1

    # 流派节点
    for liupai in data["流派分类"]["主要流派"]:
        nodes.append({
            "id": node_id,
            "label": liupai["名称"],
            "type": "流派",
            "properties": {
                "代表地区": ",".join(liupai["代表地区"]),
                "特点": liupai["特点"],
                "代表剧目": ",".join(liupai.get("代表剧目", []))
            }
        })
        node_id += 1

    # 制作工艺节点
    craft_node = {
        "id": node_id,
        "label": "制作工艺",
        "type": "工艺",
        "properties": {
            "流程数量": len(data["制作工艺"]["制作流程"]),
            "造型特点": data["制作工艺"]["造型特点"]
        }
    }
    nodes.append(craft_node)
    node_id += 1

    # 添加各制作步骤节点
    for step in data["制作工艺"]["制作流程"]:
        nodes.append({
            "id": node_id,
            "label": f"步骤{step['步骤']}-{step['名称']}",
            "type": "制作步骤",
            "properties": {
                "步骤序号": step["步骤"],
                "名称": step["名称"],
                "说明": step["说明"],
                "要点": step.get("要点", [])
            }
        })
        node_id += 1

    # 表演技法节点
    performance_node = {
        "id": node_id,
        "label": "表演技法",
        "type": "表演艺术",
        "properties": {
            "操纵技法数量": len(data["表演技法"]["操纵技法"]),
            "唱腔类型": [c["类型"] for c in data["表演技法"]["唱腔音乐"]["唱腔类型"]]
        }
    }
    nodes.append(performance_node)
    node_id += 1

    # 传承人节点
    for inheritor in data["代表传承人"]:
        nodes.append({
            "id": node_id,
            "label": inheritor["姓名"],
            "type": "传承人",
            "properties": {
                "流派": inheritor["流派"],
                "级别": inheritor["级别"],
                "简介": inheritor["简介"]
            }
        })
        node_id += 1

    # 剧目节点
    for play in data["经典剧目"]:
        nodes.append({
            "id": node_id,
            "label": play["剧名"],
            "type": "剧目",
            "properties": {
                "类型": play["类型"],
                "流派": play["流派"],
                "简介": play["简介"]
            }
        })
        node_id += 1

    return nodes

